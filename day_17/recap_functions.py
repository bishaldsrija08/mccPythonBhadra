# Function: Function is a block of reusable code that performs a specific task.

# function with no parameters and no return value
def greet(): # Define a function named 'greet'
    print("Hello, welcome to the function tutorial!") # Call the function to execute its code
greet() # Call the function to execute its code

# function with parameters and no return value
def greet_user(name): # Define a function with a parameter 'name'
    print(f"Hello, {name}! Welcome to the function tutorial!")
greet_user("Bishal") # Call the function with an argument

# function with parameters and a return value

def add(a,b):
    return a+b
result = add(1,2)
print(result)

